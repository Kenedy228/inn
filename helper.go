package inn

import "fmt"

// isDigit проверяет, является ли rune десятичной цифрой.
//
// Функция возвращает true, если символ находится в диапазоне
// от '0' до '9', и false в противном случае.
//
// Используется во внутренних функциях парсинга и валидации ИНН.
func isDigit(r rune) bool {
	return '0' <= r && r <= '9'
}

// parseDigits преобразует строковое представление ИНН в срез цифр.
//
// Каждый символ строки интерпретируется как отдельная цифра
// и преобразуется в соответствующее целое число.
//
// Например:
//
//	"123" → []int{1, 2, 3}
//
// Функция паникует, если строка содержит символы, отличные от цифр.
// Это считается ошибкой программиста, так как предполагается,
// что входная строка уже прошла предварительную валидацию.
func parseDigits(s string) []int {
	digits := make([]int, 0, len(s))

	for _, r := range s {
		if !isDigit(r) {
			panic(fmt.Sprintf("parseDigits: строка содержит нецифровой символ %q", r))
		}

		digits = append(digits, int(r-'0'))
	}

	return digits
}
