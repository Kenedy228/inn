package inn

import "strings"

// Normalize приводит строковое представление ИНН к нормализованному виду.
//
// В текущей реализации функция удаляет все пробелы из строки,
// что позволяет корректно обрабатывать ввод с разделителями:
//
//	"123 456 789 012" → "123456789012"
//
// Функция не выполняет валидацию содержимого строки и не изменяет
// другие символы. Проверка корректности ИНН должна выполняться
// отдельными функциями валидации.
func Normalize(code string) string {
	return strings.ReplaceAll(code, " ", "")
}
